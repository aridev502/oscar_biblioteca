                NOSOTROS
            </h2>
            <p class="text-center">
                El municipio de Santiago Chimaltenango es uno de los que ocupa el antepenúltimo lugar en el listado de los municipios con que cuenta Huehuetenango, en el cual se habla el idioma Maya Mam, que aún mantiene el prestigio de usar el traje típico específicamente las bellas mujeres como también las costumbres en lo que se refiere la solidaridad y el apoyo mutuo entre la población. <br> <br>

                En el transcurso del conflicto armado en la cual se quemó el edificio de la Municipalidad, a raíz de esto se desaparecieron los documentos importantes de los habitantes como las identificaciones personales, escrituras de áreas pertenecientes a la religión Católica, como el caso de la Escuela Oficial Urbana Mixta que desde ese entonces hizo historia como parte del desarrollo del municipio, contando con un 20% de población escolar, debido a la poca o nula importancia que se daba a la educación por desconocimiento de los padres de familia, las inscripciones de los alumnos se realizaban a través de visitas domiciliarias, se inscribían a la fuerza y se escondían en cualquier rincón por no querer asistir a la escuela, aunque esto se hacía por varias razones, por ejemplo una educación asistemática, autoritarista, castigos ceberos, convirtiendo a los alumnos como empleados (as) domésticos (as) de los maestros (as). <br> <br>

                Además los docentes no tenían un grado académico correspondiente, bastaba con tener sexto Primaria y se ejercía la docencia, la cual implicó deficiencias en la calidad educativa. <br> <br>

                La Escuela cuenta con 80 años aproximadamente de existencia en la población, donde en el año 1983 se tuvo como directora a la profesora Alicia Rojas y Perfilia de Pinto procedentes de la ciudad de Huehuetenango. <br> <br>

                El edificio anterior del establecimiento fue diseñado y construido por el arquitecto Manrique Sanz procedente de la ciudad de Huehuetenango, durante la administración del alcalde municipal Diego Ordóñez Díaz y el Vice Alcalde Andrés Ordoñez Méndez. <br> <br>

                El perfil y valor que se aspiraba tener en los primeros educandos era de formar hombres y mujeres con lealtad, honradez para el servicio de la Patria que los vio nacer. <br> <br>

                Teniendo el conocimiento o la comparación de la historia de la Escuela desde su origen hasta la actualidad ha tenido una diferencia radical según lo expuesto anteriormente que se empezó a educar a 55 alumnos aproximadamente desde la etapa de Castellanización en el año 1938. <br> <br> Hoy en día ya se cuenta con 541 alumnos, con 28 docentes graduados académicamente, 1 maestro de educación física, 1 director administrativo y 1 personal de servicio de los cuales el 90% son originarios y residentes del municipio. <br> <br>

                Actualmente el establecimiento brinda servicios educativos en los niveles parvulario, pre-primario y primario en la modalidad monolingüe y bilingüe, se imparten clases en 28 salones de clases, y un salón que ocupa la dirección administrativa, área para la elaboración de la refacción, servicios sanitarios y patio de recreación. <br> <br> Asimismo se cuenta con la Organización de Padres de Familia quienes también apoyan la educación de la niñez. <br> <br>

                Desde la creación del centro educativo hasta la actualidad atiende a la población estudiantil de manera mixta, enfocado al criterio docente en relación de la metodología y métodos del proceso enseñanza aprendizaje y evaluación. <br> <br>

